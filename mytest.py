import ipaddress

def find_overlapping_cidrs(cidr_list):
    overlapping_cidrs = []

    for i in range(len(cidr_list)):
        for j in range(i+1, len(cidr_list)):
            cidr1 = ipaddress.ip_network(cidr_list[i])
            cidr2 = ipaddress.ip_network(cidr_list[j])

            if cidr1.overlaps(cidr2):
                overlapping_cidrs.append((str(cidr1), str(cidr2)))

    return overlapping_cidrs

